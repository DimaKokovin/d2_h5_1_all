// С помощью цикла while найдите сумму чисел от 1 до 20
// Результат присвойте переменной sum

// your code
let sum;

let count = 1;
let number = 0;
let numbers = 1;

while (count <= 20) {
  count += 1;
  number = number + numbers;
  numbers += 1;
  sum = number;
}

